//"3. Create a class Dog.
//The dog should have a name and an age (private).
//Create getters and setter for all the fields."
public class Dog {
    private int age;
    private String name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
